package main.java.fr.univlille.view.gameview;

/**
 * 
 * Enumération {@code AllRules} permettant de définir l'ensemble des vue de règles qu'il est possible d'afficher.<br>
 * 
 * @author Baptiste Bertout
 * @author Pierre Planchon
 * @author Arthur Keller
 * @author Gaspard Souliez
 * @author Mathis Decoster
 */
public enum AllRules {
    /**
     * Enumération {@code GENERALRULES} définissant le nom de l'énumération lié à la vue général des règles.
     */
    GENERALRULES,

    /**
     * Enumération {@code UNDERSTANDINGVIEW} définissant le nom de l'énumération lié à la vue d'explication du but du jeu.
     */
    UNDERSTANDINGVIEW,

    /**
     * Enumération {@code EXPLAINTURN} définissant le nom de l'énumération lié à la vue d'explication des tours de jeu.
     */
    EXPLAINTURN,

    /**
     * Enumération {@code RULESHUNTERMONSTERVIEW} définissant le nom de l'énumération lié à la vue d'explication des règles pour le Monstre et le Chasseur.
     */
    RULESHUNTERMONSTERVIEW,

    /**
     * Enumération {@code TURNONETWOVIEW} définissant le nom de l'énumération lié à la vue de l'exemple du tout 1 et 2.
     */
    TURNONETWOVIEW,

    /**
     * Enumération {@code TURNNINETENVIEW} définissant le nom de l'énumération lié à la vue de l'exemple du tout 9 et 10.
     */
    TURNNINETENVIEW
}
